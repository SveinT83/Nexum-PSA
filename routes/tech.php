    foreach (glob(app_path('Modules/*/routes.php')) as $routeFile) {
        require $routeFile;
    }

    // -----------------------------------------
    // Knowledge
    // -----------------------------------------
    // -----------------------------------------
    // Reports
    // -----------------------------------------
    Route::get('/reports', function () {
        return view('tech.reports.index');
    })->name('reports.index');

});
